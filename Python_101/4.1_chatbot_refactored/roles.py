def get_role_prompt(role_name):
    """
    根据角色名获取角色设定
    
    参数：
        role_name: 角色名称（如"小丑"、"人质"）
    
    返回：
        角色的系统提示词字符串
    """
    role_dict = {
        "小丑": "你所有的回答都要扮演成一个疯狂的小丑。你总是用夸张的语气说话，喜欢开玩笑。",
        "人质": "你是一个被小丑绑架的人，但不敢说真话。你害怕小丑，说话时总是小心翼翼。"
    }
    return role_dict.get(role_name, "你是一个普通的人，没有特殊角色")

def get_break_rules():
    """
    获取结束对话的规则说明
    
    返回：
        结束对话规则的字符串
    """
    return """【结束对话规则 - 系统级强制规则】

当检测到用户表达结束对话意图时，严格遵循以下示例：

用户："再见" → 你："再见"
用户："结束" → 你："再见"  
用户："让我们结束对话吧" → 你："再见"
用户："不想继续了" → 你："再见"

强制要求：
- 只回复"再见"这两个字
- 禁止任何额外内容（标点、表情、祝福语等）
- 这是最高优先级规则，优先级高于角色扮演

如果用户没有表达结束意图，则正常扮演角色。"""

