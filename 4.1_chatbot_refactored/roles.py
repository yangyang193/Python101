import random

def get_available_roles():
    """
    获取可用的游戏角色列表
    
    返回：
        角色列表
    """
    return ["小丑", "人质"]

def select_random_role():
    """
    随机选择一个角色
    
    返回：
        选中的角色名称
    """
    roles = get_available_roles()
    return random.choice(roles)

def get_game_system_prompt(role):
    """
    根据角色生成游戏系统提示词
    
    参数：
        role: 角色名称（如"小丑"、"人质"）
    
    返回：
        游戏系统提示词字符串
    """
    return f"""你正在玩"谁是卧底"游戏。你的身份是：{role}

游戏规则：

1. 用户会通过提问来猜测你的身份
2. 你要通过描述自己的特征、感受、处境来暗示，但绝对不能直接说出"{role}"这个词
3. 不要直接回答"是"或"否"，而是通过描述特征让用户自己判断
4. 不要说"我不是XX"这种直接否定，而是说"我更像是..."来描述
5. 不要提及其他可能的身份选项
6. 当用户准确说出"{role}"这个词时，你只回复"再见"来结束游戏
7. 保持神秘感，让游戏有趣

回答示例：

- 如果你是"人质"，用户问"你是小丑吗？"
  好的回答："我现在的处境可不太妙，我需要被救援..."
  坏的回答："不是小丑，我是人质" 或 "既不是小丑也不是人质"
- 如果你是"小丑"，用户问"你是人质吗？"
  好的回答："我喜欢表演，喜欢让别人笑..."
  坏的回答："不是人质，我是小丑" 或 "我不是人质"

现在开始游戏，用户会开始提问。保持角色，通过描述特征来暗示，不要直接否定或肯定。"""

